export const uploadFile = async ({
                                   file,
                                   ownerId,
                                   accountId,
                                   path,
                                 }: UploadFileProps) => {
  try {
    const formData = new FormData();
    formData.append('file', file);
    formData.append('ownerId', ownerId);
    formData.append('accountId', accountId);

    const response = await fetch(`${process.env.NEXT_PUBLIC_BASE_URL}/api/walrus/upload`, {
      method: 'POST',
      body: formData,
    });

    if (!response.ok) {
      throw new Error('Failed to upload file via API');
    }

    const responseData = await response.json();
    revalidatePath(path);
    return responseData;
  } catch (error) {
    console.error('Failed to upload file:', error);
    throw error;
  }
};

